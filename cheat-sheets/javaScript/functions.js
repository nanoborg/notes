

// Function Declaration

function add(num) {
    return num + 2;
}


// Function Expression
const add = function(num) {
    return num + 2;
}

// Arrow Function Expression
const add = (num) => {
    return num + 2;
}

const addTogether = (num1, num2) => {
    return num1 + num2;
}


// Concise Arrow Function Expression
const add = num => num + 2;